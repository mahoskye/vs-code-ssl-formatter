#!/usr/bin/env node
/**
 * Extract diagnostic-code slugs from the sibling starlims-lsp repo and
 * propagate them to two targets:
 *
 *   1. src/constants/diagnosticSlugs.ts — runtime constant the extension
 *      can use to validate user-supplied rule names (currently unused but
 *      kept ready for future surfaces like the status-bar tooltip).
 *   2. package.json contributes.configuration.properties["ssl.diagnostics.rules"]
 *      .propertyNames.enum — gives VS Code's settings UI autocomplete on
 *      rule slug keys.
 *
 * Run via `npm run generate:slugs` after updating the bundled LSP. The
 * sibling repo path can be overridden with the LSP_REPO env var.
 *
 * Source of truth: starlims-lsp/internal/providers/diagnostic_codes.go.
 * Each `Code... = "<slug>"` line yields one slug; comments and other
 * declarations are ignored.
 */
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = resolve(__dirname, '..');
const LSP_REPO = process.env.LSP_REPO ||
    resolve(PROJECT_ROOT, '..', 'starlims-lsp');
const SOURCE_FILE = resolve(LSP_REPO, 'internal/providers/diagnostic_codes.go');
const TS_OUTPUT = resolve(PROJECT_ROOT, 'src/constants/diagnosticSlugs.ts');
const PACKAGE_JSON = resolve(PROJECT_ROOT, 'package.json');

function extractSlugs(goSource) {
    const slugs = new Set();
    const re = /^\s*Code\w+\s*=\s*"([a-z0-9_]+)"/gm;
    let m;
    while ((m = re.exec(goSource)) !== null) {
        slugs.add(m[1]);
    }
    return [...slugs].sort();
}

function writeTsConstant(slugs) {
    const body = `// AUTO-GENERATED by scripts/generate-diagnostic-slugs.mjs.
// Source: ../starlims-lsp/internal/providers/diagnostic_codes.go.
// Run \`npm run generate:slugs\` after bumping the bundled LSP.

export const SSL_DIAGNOSTIC_SLUGS = [
${slugs.map(s => `    "${s}"`).join(',\n')}
] as const;

export type SSLDiagnosticSlug = typeof SSL_DIAGNOSTIC_SLUGS[number];
`;
    writeFileSync(TS_OUTPUT, body, 'utf-8');
}

function patchPackageJson(slugs) {
    const raw = readFileSync(PACKAGE_JSON, 'utf-8');
    const pkg = JSON.parse(raw);
    const prop = pkg.contributes?.configuration?.properties?.['ssl.diagnostics.rules'];
    if (!prop) {
        throw new Error('ssl.diagnostics.rules property missing from package.json');
    }
    prop.propertyNames = {
        enum: slugs,
        description: 'Stable rule slug emitted by starlims-lsp. See diagnostic_codes.go.'
    };
    // Preserve existing two-space indentation.
    writeFileSync(PACKAGE_JSON, JSON.stringify(pkg, null, 4) + '\n', 'utf-8');
}

function main() {
    let goSource;
    try {
        goSource = readFileSync(SOURCE_FILE, 'utf-8');
    } catch (err) {
        console.error(`Could not read ${SOURCE_FILE}: ${err.message}`);
        console.error('Set LSP_REPO env var if the sibling repo is not at ../starlims-lsp.');
        process.exit(1);
    }
    const slugs = extractSlugs(goSource);
    if (slugs.length === 0) {
        console.error('No slugs extracted — check the source file format.');
        process.exit(1);
    }
    writeTsConstant(slugs);
    patchPackageJson(slugs);
    console.log(`Wrote ${slugs.length} slugs to:`);
    console.log(`  ${TS_OUTPUT}`);
    console.log(`  ${PACKAGE_JSON} (ssl.diagnostics.rules.propertyNames.enum)`);
}

main();
